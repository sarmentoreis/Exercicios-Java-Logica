package com.leonardo.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int valor1 = 1;
		int valor2 = 2;
		
		//Operadores relacionais retornam um boolean.
		System.out.println("valor1 == valor2 " + (valor1 == valor2));
		System.out.println("valor1 != valor2 " + (valor1 != valor2));
		System.out.println("valor1 > valor2 " + (valor1 > valor2));
		System.out.println("valor1 >= valor2 " + (valor1 >= valor2));
		System.out.println("valor1 < valor2 " + (valor1 < valor2));
		System.out.println("valor1 <= valor2 " + (valor1 <= valor2));



	}

}
