package com.leonardo.cursojava.aulaEx7Static;

import java.text.DecimalFormat;

public class ConversaoDeUnidadesDeArea {
	
	
	public static double metrosToPes(double m) {
		
		return m * 10.764;
	}
	
	public static double peToCm(double pe) {
		return pe * 929;
	}
	
	public static double milhaToAcre(double milha) {
		return milha * 640;
	}
	
	public static double acreToPes(double acre) {
		return acre * 43560;
	}
}
